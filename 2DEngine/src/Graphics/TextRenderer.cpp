void TextRenderer::Init()
{
    s_Data = std::make_unique<TextData>();
    s_Data->IsInitialized = true;
    std::cout << "TextRenderer initialized with built-in bitmap font\n";
}

void TextRenderer::Shutdown()
{
    s_Data.reset();
}

